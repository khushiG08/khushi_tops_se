#include <iostream>
using namespace std;

int main()
{
  double a, b;

  cout << "Enter the first number: " << endl;
  cin >> a;

  cout << "Enter the second number: " << endl;
  cin >> b;

  cout << "Addition = " << a + b << endl;

  cout << "Subtraction = " << a - b << endl;

  cout << "Multiplication = " << a * b << endl;

  cout << "Division = " << a / b << endl;
}